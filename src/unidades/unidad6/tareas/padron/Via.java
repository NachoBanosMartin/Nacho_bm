package unidades.unidad6.tareas.padron;

public class Via {

	private String tipo;
	private int numero;
	private char letra;
	private String nombre;
	private double km;
	private String bloque;
	private String portal;
	private int escalera;
	private int planta;
	private char puerta;
	
	
	public Via(String tipo, int numero, char letra, String nombre, double km, String bloque, String portal,
			int escalera, int planta, char puerta) {
		super();
		this.tipo = tipo;
		this.numero = numero;
		this.letra = letra;
		this.nombre = nombre;
		this.km = km;
		this.bloque = bloque;
		this.portal = portal;
		this.escalera = escalera;
		this.planta = planta;
		this.puerta = puerta;
	}
	
	
}
